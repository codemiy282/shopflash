        -- 1. Check if user exists
        IF NOT EXISTS (SELECT 1 FROM USERS WHERE USER_ID = p_UserID) THEN
            SET v_LogMessage = CONCAT('User ', p_UserID, ' does not exist.'); -- NOTE: ADDING SET TO AVOID ERROR
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = v_LogMessage;
        END IF;
        
        -- 2. Prevent deletion of system user (user000000)
        IF p_UserID = 'user000000' THEN
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Cannot delete system user (user000000).';
        END IF;
        
        -- 3. Check if system user exists in BUYER table (for data transfer)
        IF NOT EXISTS (SELECT 1 FROM BUYER WHERE USER_ID = 'user000000') THEN
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'System user (user000000) does not exist in BUYER table. Please create system user first.';
        END IF;
        
        -- 4. Determine user roles
        SET v_IsBuyer = EXISTS (SELECT 1 FROM BUYER WHERE USER_ID = p_UserID);
        SET v_IsSeller = EXISTS (SELECT 1 FROM SELLER WHERE USER_ID = p_UserID);
        SET v_IsStaff = EXISTS (SELECT 1 FROM STAFF WHERE USER_ID = p_UserID);
        SET v_IsAdmin = EXISTS (SELECT 1 FROM ADMIN WHERE USER_ID = p_UserID);
        
        -- Log user deletion information
        SET v_LogMessage = CONCAT('Deleting user ', p_UserID, 
                                '. Roles: Buyer=', v_IsBuyer,
                                ', Seller=', v_IsSeller,
                                ', Staff=', v_IsStaff,
                                ', Admin=', v_IsAdmin);
        
        -- 5. Handle BUYER data preservation
        IF v_IsBuyer = TRUE THEN
            -- Transfer orders to system user
            UPDATE ORDERS 
            SET USER_ID = 'user000000'
            WHERE USER_ID = p_UserID;
            
            -- Transfer reviews to system user
            UPDATE REVIEW
            SET USER_ID = 'user000000'
            WHERE USER_ID = p_UserID;
            
            -- Delete wishlists (cascade would handle, but explicit for clarity)
            DELETE FROM WISHLIST_VARIANT WHERE BUYER_ID = p_UserID;
            DELETE FROM WISHLIST WHERE BUYER_ID = p_UserID;
            
            -- Delete buyer record
            DELETE FROM BUYER WHERE USER_ID = p_UserID;
        END IF;
        
        -- 6. Handle SELLER data preservation
        IF v_IsSeller = TRUE THEN
            -- Mark seller products as INACTIVE instead of deleting
            UPDATE PRODUCT
            SET DISPLAY_STATUS = 'INACTIVE'
            WHERE STORE_ID IN (SELECT STORE_ID FROM STORE WHERE SELLER_ID = p_UserID);
            
            -- Delete seller record (stores will cascade delete)
            DELETE FROM SELLER WHERE USER_ID = p_UserID;
        END IF;
        
        -- 7. Delete other roles
        IF v_IsAdmin = TRUE THEN
            DELETE FROM ADMIN WHERE USER_ID = p_UserID;
        END IF;
        
        IF v_IsStaff = TRUE THEN
            DELETE FROM STAFF WHERE USER_ID = p_UserID;
        END IF;
        
        -- 8. Delete addresses (cascade would handle, but explicit for clarity)
        DELETE FROM ADDRESS WHERE USER_ID = p_UserID;
        
        -- 9. Finally delete the user from USERS table
        DELETE FROM USERS WHERE USER_ID = p_UserID;
        
        COMMIT;
        
        -- Return success message with details
        SELECT 'Success' AS Status, 
            CONCAT('User ', p_UserID, ' deleted successfully with data preservation.') AS Message,
            v_IsBuyer AS WasBuyer,
            v_IsSeller AS WasSeller,
            v_IsAdmin AS WasAdmin,
            v_IsStaff AS WasStaff;
