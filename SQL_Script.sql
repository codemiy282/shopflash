        VERIFICATION_STATUS VARCHAR(20)        NOT NULL,  -- Account verification: verified, pending, rejected
        COMMISSION_RATE     DECIMAL(5,2)       NOT NULL,  -- Platform commission percentage (0-100 with 2 decimals)
