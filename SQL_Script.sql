/* ==============================================================
   FUNCTION: GetProductRatingStatistics
   PURPOSE: Get rating statistics for products within rating range
   DESCRIPTION:
   - Returns table of products with rating statistics
   - Calculates average, min, max ratings
   - Categorizes products based on average rating
   - Only includes approved reviews
   ============================================================== */
DELIMITER $$
CREATE FUNCTION GetProductRatingStatistics(
    p_MinRating INT, -- Minimum rating to include (1-5)
    p_MaxRating INT  -- Maximum rating to include (1-5)
)
RETURNS TEXT
DETERMINISTIC
BEGIN
    DECLARE result TEXT DEFAULT '';
    
    SELECT GROUP_CONCAT(CONCAT_WS('|', 
        p.PRODUCT_ID,
        p.NAME,
        s.NAME,
        COUNT(r.REVIEW_ID),
        ROUND(AVG(r.RATING_SCORE), 2),
        MIN(r.RATING_SCORE),
        MAX(r.RATING_SCORE),
        CASE
            WHEN AVG(r.RATING_SCORE) >= 4.5 THEN 'Excellent'
            WHEN AVG(r.RATING_SCORE) >= 4.0 THEN 'Good'
            WHEN AVG(r.RATING_SCORE) >= 3.0 THEN 'Average'
            ELSE 'Poor'
        END
    ) SEPARATOR ';')
    INTO result
    FROM REVIEW r
    INNER JOIN ORDER_LINE_ITEM oli 
        ON r.LINE_ITEM_ID = oli.LINE_ITEM_ID 
       AND r.ORDER_ID = oli.ORDER_ID
    INNER JOIN ORDER_LINE_ITEM_VARIANT oliv 
        ON oli.LINE_ITEM_ID = oliv.LINE_ITEM_ID 
       AND oli.ORDER_ID = oliv.ORDER_ID
    INNER JOIN PRODUCT_VARIANT pv 
        ON oliv.VARIANT_ID = pv.VARIANT_ID 
       AND oliv.PRODUCT_ID = pv.PRODUCT_ID
    INNER JOIN PRODUCT p 
        ON pv.PRODUCT_ID = p.PRODUCT_ID
    INNER JOIN STORE s 
        ON p.STORE_ID = s.STORE_ID
    WHERE r.MODERATION_STATUS = 'approved'
      AND r.RATING_SCORE BETWEEN p_MinRating AND p_MaxRating
    GROUP BY p.PRODUCT_ID, p.NAME, s.NAME
    HAVING COUNT(r.REVIEW_ID) >= 1;
    
    RETURN result;
END$$
DELIMITER;

/* ==============================================================
   STORED PROCEDURE: GetTopSellingProducts
   PURPOSE: Get top selling products with various filters
   DESCRIPTION:
   - Returns top N selling products based on revenue
   - Supports filtering by store, category, date range
   - Calculates sales metrics and categorizes products
   - Validates input parameters
   ============================================================== */
DELIMITER $$
CREATE PROCEDURE GetTopSellingProducts(
    IN p_StoreID INT,      -- Optional: Filter by store ID
    IN p_CategoryID INT,   -- Optional: Filter by category ID
    IN p_StartDate DATETIME, -- Optional: Filter by start date
    IN p_EndDate DATETIME,   -- Optional: Filter by end date
    IN p_TopN INT           -- Number of top products to return (1-100)
)
BEGIN
    -- Validate TopN parameter
    IF p_TopN <= 0 OR p_TopN > 100 THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'TopN must be between 1 and 100.';
    END IF;

    -- Validate date range if both dates provided
    IF p_StartDate IS NOT NULL AND p_EndDate IS NOT NULL AND p_StartDate > p_EndDate THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Start date must be before end date.';
    END IF;

    -- Return top N selling products with calculated metrics
    -- Use prepared statement for dynamic LIMIT 
    -- NOTE: MySQL does not allow LIMIT with parameters directly, using dynamic SQL
    SET @sql_query = CONCAT(' 
        SELECT 
            p.PRODUCT_ID,
            p.NAME AS ProductName,
            c.NAME AS CategoryName,
            s.NAME AS StoreName,
            COUNT(DISTINCT o.ORDER_ID) AS TotalOrders,
            SUM(oli.QUANTITY) AS TotalQuantitySold,
                SUM(oli.SUBTOTAL) AS TotalRevenue,
                ROUND(AVG(oli.UNIT_PRICE), 2) AS AverageUnitPrice,
            CASE
                WHEN SUM(oli.QUANTITY) >= 20 OR SUM(oli.SUBTOTAL) >= 300000 
                    THEN ''Best Seller''
                WHEN SUM(oli.QUANTITY) >= 10 OR SUM(oli.SUBTOTAL) >= 150000 
                    THEN ''Popular''
                WHEN SUM(oli.QUANTITY) >= 3 OR SUM(oli.SUBTOTAL) >= 50000 
                    THEN ''Regular''
                ELSE ''Low Volume''
            END AS SalesCategory
        FROM PRODUCT p
        LEFT JOIN CATEGORY c ON p.CATEGORY_ID = c.CATEGORY_ID
        LEFT JOIN STORE s ON p.STORE_ID = s.STORE_ID
        LEFT JOIN PRODUCT_VARIANT pv ON p.PRODUCT_ID = pv.PRODUCT_ID
        LEFT JOIN ORDER_LINE_ITEM_VARIANT oliv 
            ON pv.VARIANT_ID = oliv.VARIANT_ID 
           AND pv.PRODUCT_ID = oliv.PRODUCT_ID
        LEFT JOIN ORDER_LINE_ITEM oli 
            ON oliv.LINE_ITEM_ID = oli.LINE_ITEM_ID 
           AND oliv.ORDER_ID = oliv.ORDER_ID
        LEFT JOIN ORDERS o 
            ON oli.ORDER_ID = o.ORDER_ID
        WHERE 1=1
            AND (', IFNULL(p_StoreID, 'NULL'), ' IS NULL OR p.STORE_ID = ', IFNULL(p_StoreID, 'NULL'), ')
            AND (', IFNULL(p_CategoryID, 'NULL'), ' IS NULL OR p.CATEGORY_ID = ', IFNULL(p_CategoryID, 'NULL'), ')
            AND (', IFNULL(CONCAT('''', p_StartDate, ''''), 'NULL'), ' IS NULL OR o.CREATED_TIMESTAMP >= ', IFNULL(CONCAT('''', p_StartDate, ''''), 'NULL'), ')
            AND (', IFNULL(CONCAT('''', p_EndDate, ''''), 'NULL'), ' IS NULL OR o.CREATED_TIMESTAMP <= ', IFNULL(CONCAT('''', p_EndDate, ''''), 'NULL'), ')
            AND o.STATUS IN (''delivered'', ''shipped'')
        GROUP BY p.PRODUCT_ID, p.NAME, c.NAME, s.NAME
        HAVING SUM(oli.QUANTITY) IS NOT NULL
        ORDER BY TotalRevenue DESC, TotalQuantitySold DESC
        LIMIT ', p_TopN);
    
    PREPARE stmt FROM @sql_query;
    EXECUTE stmt;
    DEALLOCATE PREPARE stmt;
END$$
DELIMITER ;

/* ==============================================================
   STORED PROCEDURE: CalculateSellerCommission
   PURPOSE: Calculate commission for sellers for specific month
   DESCRIPTION:
   - Calculates commission based on sales and commission rate
   - Supports individual seller or all sellers
   - Categorizes sellers by sales volume (Gold, Silver, etc.)
   - Validates month and year parameters
   ============================================================== */
DELIMITER $$
CREATE PROCEDURE CalculateSellerCommission(
    IN p_SellerID VARCHAR(10), -- Optional: Specific seller ID
    IN p_Month INT,           -- Required: Month (1-12)
    IN p_Year INT             -- Required: Year (2000-current)
)
BEGIN
    DECLARE v_StartDate DATETIME;
    DECLARE v_EndDate DATETIME;
    
    -- Validate month parameter
    IF p_Month < 1 OR p_Month > 12 THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Month must be between 1 and 12.';
    END IF;
    
    -- Validate year parameter
    IF p_Year < 2000 OR p_Year > YEAR(CURRENT_DATE()) THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Invalid year.';
    END IF;
    
    -- Calculate date range for the specified month
    SET v_StartDate = STR_TO_DATE(CONCAT(p_Year, '-', p_Month, '-01'), '%Y-%m-%d');
    SET v_EndDate = LAST_DAY(v_StartDate);
    
    -- Branch based on whether specific seller or all sellers requested
    IF p_SellerID IS NOT NULL THEN
        -- Single seller commission calculation
        SELECT 
            sel.USER_ID AS SellerID,
            u.USERNAME AS SellerName,
            st.NAME AS StoreName,
            COUNT(DISTINCT o.ORDER_ID) AS TotalOrders,
            SUM(o.TOTAL_MONEY) AS TotalSales,
            sel.COMMISSION_RATE AS CommissionRate,
            SUM(o.TOTAL_MONEY) * (sel.COMMISSION_RATE / 100) AS CommissionAmount,
            CASE
                WHEN SUM(o.TOTAL_MONEY) > 10000 THEN 'Gold Level'
                WHEN SUM(o.TOTAL_MONEY) > 5000 THEN 'Silver Level'
                WHEN SUM(o.TOTAL_MONEY) > 1000 THEN 'Bronze Level'
                ELSE 'Standard Level'
            END AS SellerLevel
        FROM SELLER sel
        INNER JOIN USERS u ON sel.USER_ID = u.USER_ID
        INNER JOIN STORE st ON sel.USER_ID = st.SELLER_ID
        INNER JOIN PRODUCT p ON st.STORE_ID = p.STORE_ID
        INNER JOIN PRODUCT_VARIANT pv ON p.PRODUCT_ID = pv.PRODUCT_ID
        INNER JOIN ORDER_LINE_ITEM_VARIANT oliv ON pv.VARIANT_ID = oliv.VARIANT_ID AND pv.PRODUCT_ID = oliv.PRODUCT_ID
        INNER JOIN ORDER_LINE_ITEM oli ON oliv.LINE_ITEM_ID = oli.LINE_ITEM_ID AND oliv.ORDER_ID = oli.ORDER_ID
        INNER JOIN ORDERS o ON oli.ORDER_ID = o.ORDER_ID
        WHERE sel.USER_ID = p_SellerID
            AND o.CREATED_TIMESTAMP BETWEEN v_StartDate AND v_EndDate
            AND o.STATUS IN ('delivered', 'shipped')
        GROUP BY sel.USER_ID, u.USERNAME, st.NAME, sel.COMMISSION_RATE
        HAVING COUNT(DISTINCT o.ORDER_ID) > 0;
    ELSE
        -- All sellers commission calculation
        SELECT 
            sel.USER_ID AS SellerID,
            u.USERNAME AS SellerName,
            st.NAME AS StoreName,
            COUNT(DISTINCT o.ORDER_ID) AS TotalOrders,
            SUM(o.TOTAL_MONEY) AS TotalSales,
            sel.COMMISSION_RATE AS CommissionRate,
            SUM(o.TOTAL_MONEY) * (sel.COMMISSION_RATE / 100) AS CommissionAmount,
            CASE
                WHEN SUM(o.TOTAL_MONEY) > 10000 THEN 'Gold Level'
                WHEN SUM(o.TOTAL_MONEY) > 5000 THEN 'Silver Level'
                WHEN SUM(o.TOTAL_MONEY) > 1000 THEN 'Bronze Level'
                ELSE 'Standard Level'
            END AS SellerLevel
        FROM SELLER sel
        INNER JOIN USERS u ON sel.USER_ID = u.USER_ID
        INNER JOIN STORE st ON sel.USER_ID = st.SELLER_ID
        INNER JOIN PRODUCT p ON st.STORE_ID = p.STORE_ID
        INNER JOIN PRODUCT_VARIANT pv ON p.PRODUCT_ID = pv.PRODUCT_ID
        INNER JOIN ORDER_LINE_ITEM_VARIANT oliv ON pv.VARIANT_ID = oliv.VARIANT_ID AND pv.PRODUCT_ID = oliv.PRODUCT_ID
        INNER JOIN ORDER_LINE_ITEM oli ON oliv.LINE_ITEM_ID = oli.LINE_ITEM_ID AND oliv.ORDER_ID = oli.ORDER_ID
        INNER JOIN ORDERS o ON oli.ORDER_ID = o.ORDER_ID
        WHERE o.CREATED_TIMESTAMP BETWEEN v_StartDate AND v_EndDate
            AND o.STATUS IN ('delivered', 'shipped')
        GROUP BY sel.USER_ID, u.USERNAME, st.NAME, sel.COMMISSION_RATE
        HAVING COUNT(DISTINCT o.ORDER_ID) > 0
        ORDER BY CommissionAmount DESC, TotalSales DESC;
    END IF;
END$$
DELIMITER ;

/* ==============================================================
   TRIGGER: trg_enforce_salary_rules
   PURPOSE: Enforce salary rules for staff positions
   DESCRIPTION:
   - Ensures minimum wage compliance
   - Enforces salary hierarchy (order managers > support staff)
   - Limits salary increases to 50% maximum
   - Logs salary changes to EVENT_LOG
   ============================================================== */
DELIMITER $$
CREATE TRIGGER trg_enforce_salary_rules
BEFORE INSERT ON STAFF
FOR EACH ROW
BEGIN
    DECLARE v_MinWage DECIMAL(18,2) DEFAULT 30000.00;
    DECLARE v_MinOrderManagerWage DECIMAL(18,2) DEFAULT 50000.00;
    DECLARE v_MinOrderManagerSalary DECIMAL(18,2);
    DECLARE v_MaxSupportSalary DECIMAL(18,2);
    
    -- 1. Minimum wage check
    IF NEW.SALARY < v_MinWage THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Salary cannot be below minimum wage (30,000).';
    END IF;
    
    -- 2. Order manager minimum salary
    IF NEW.POSITION = 'order_manager' AND NEW.SALARY < v_MinOrderManagerWage THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Order managers must earn at least 50,000.';
    END IF;
END$$
DELIMITER ;

DELIMITER $$
CREATE TRIGGER trg_enforce_salary_rules_update
BEFORE UPDATE ON STAFF
FOR EACH ROW
BEGIN
    DECLARE v_MinWage DECIMAL(18,2) DEFAULT 30000.00;
    DECLARE v_MinOrderManagerWage DECIMAL(18,2) DEFAULT 50000.00;
    DECLARE v_MinOrderManagerSalary DECIMAL(18,2);
    DECLARE v_MaxSupportSalary DECIMAL(18,2);
    
    -- 1. Minimum wage check
    IF NEW.SALARY < v_MinWage THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Salary cannot be below minimum wage (30,000).';
    END IF;
    
    -- 2. Order manager minimum salary
    IF NEW.POSITION = 'order_manager' AND NEW.SALARY < v_MinOrderManagerWage THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Order managers must earn at least 50,000.';
    END IF;
    
    -- 3. Salary hierarchy: order managers must earn more than support staff
    IF NEW.POSITION = 'order_manager' THEN
        SELECT MAX(SALARY) INTO v_MaxSupportSalary
        FROM STAFF 
        WHERE POSITION = 'support' 
          AND EMPLOYMENT_STATUS = 'active'
          AND USER_ID != NEW.USER_ID;
        
        IF v_MaxSupportSalary IS NOT NULL AND NEW.SALARY <= v_MaxSupportSalary THEN
            SET @message = CONCAT('Order managers must earn more than support staff. Max support salary: ', v_MaxSupportSalary); -- NOTE: ADDING SET TO AVOID ERROR
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = @message;
        END IF;
    END IF;
    
    -- 4. Salary increase limit: cannot increase salary by more than 50% at once
    IF NEW.SALARY > OLD.SALARY * 1.5 THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Salary increase cannot exceed 50% in one update.';
    END IF;
    
    -- 5. Log salary changes to EVENT_LOG
    IF NEW.SALARY <> OLD.SALARY THEN
        INSERT INTO EVENT_LOG (LOG_ID, EVENT_TYPE, EVENT_ACTION, ACTOR_ID, TARGET_ENTITY, TARGET_ID, CHANGE_DETAILS, STATUS)
        VALUES (
            CONCAT('LOG', LPAD((SELECT IFNULL(MAX(CAST(SUBSTRING(LOG_ID, 4) AS UNSIGNED)), 0) + 1 FROM EVENT_LOG), 7, '0')),
            'staff', 
            'salary_update', 
            USER(), 
            'staff', 
            NEW.USER_ID, 
            CONCAT('Salary changed from ', OLD.SALARY, ' to ', NEW.SALARY), 
            'active'
        );
    END IF;
END$$
DELIMITER ;


