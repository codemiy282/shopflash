    /* ==============================================================
    TRIGGER: trg_calc_order_line_item_discount_subtotal
    PURPOSE: Automatically calculate discount amounts and subtotals for order line items
    DESCRIPTION:
    - Calculates DISCOUNT based on promotion type and rules
    - Calculates SUBTOTAL as (quantity * unit_price) - discount
    - Handles percentage discounts with maximum caps
    - Handles fixed amount discounts with maximum caps
    - Executes BEFORE INSERT, UPDATE to ensure calculations are always current
    ============================================================== */
    DELIMITER $$
    CREATE TRIGGER trg_calc_order_line_item_discount_subtotal
    BEFORE INSERT ON ORDER_LINE_ITEM
    FOR EACH ROW
    BEGIN
        DECLARE discount_type VARCHAR(20);
        DECLARE discount_value DECIMAL(10,2);
        DECLARE max_discount_amount DECIMAL(10,2);
        DECLARE calculated_discount DECIMAL(18,2);
        
        -- Get promotion details if promotion exists
        IF NEW.PROMOTION_ID IS NOT NULL THEN
            SELECT DISCOUNT_TYPE, DISCOUNT_VALUE, MAX_DISCOUNT_AMOUNT
            INTO discount_type, discount_value, max_discount_amount
            FROM PROMOTION
            WHERE PROMOTION_ID = NEW.PROMOTION_ID;
            
            -- Calculate discount based on type
            IF discount_type = 'percentage' THEN
                SET calculated_discount = NEW.QUANTITY * NEW.UNIT_PRICE * (discount_value / 100);
                
                -- Apply maximum cap if exists
                IF max_discount_amount IS NOT NULL AND calculated_discount > max_discount_amount THEN
                    SET calculated_discount = max_discount_amount;
                END IF;
            ELSEIF discount_type = 'fixed' THEN
                SET calculated_discount = NEW.QUANTITY * discount_value;
                
                -- Apply maximum cap if exists
                IF max_discount_amount IS NOT NULL AND calculated_discount > max_discount_amount THEN
                    SET calculated_discount = max_discount_amount;
                END IF;
            ELSE
                SET calculated_discount = 0;  -- free_shipping or other types
            END IF;
        ELSE
            SET calculated_discount = 0;
        END IF;
        
        -- Ensure discount is not negative and not greater than total price
        IF calculated_discount < 0 THEN
            SET calculated_discount = 0;
        END IF;
        
        IF calculated_discount > NEW.QUANTITY * NEW.UNIT_PRICE THEN
            SET calculated_discount = NEW.QUANTITY * NEW.UNIT_PRICE;
        END IF;
        
        -- Set discount
        SET NEW.DISCOUNT = calculated_discount;
        
        -- Calculate subtotal
        SET NEW.SUBTOTAL = NEW.QUANTITY * NEW.UNIT_PRICE - NEW.DISCOUNT;
        
        -- Ensure subtotal is not negative
        IF NEW.SUBTOTAL < 0 THEN
            SET NEW.SUBTOTAL = 0;
        END IF;
    END$$
    DELIMITER ;

