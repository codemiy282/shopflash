/* ==============================================================
   FUNCTION: CalculateTotalRevenueByStore
   PURPOSE: Calculate total revenue for a store within date range
   DESCRIPTION:
   - Returns revenue from delivered/shipped orders
   - Validates input parameters
   - Handles inclusive date ranges
   ============================================================== */
DELIMITER $$
CREATE FUNCTION CalculateTotalRevenueByStore(
    p_StoreID INT,           -- Store ID to calculate revenue for
    p_StartDate DATETIME,    -- Start date of period
    p_EndDate DATETIME       -- End date of period
)
RETURNS DECIMAL(18,2)
DETERMINISTIC
BEGIN
    DECLARE v_TotalRevenue DECIMAL(18,2) DEFAULT 0;
    
    -- Validate date range: start date must be before or equal to end date
    IF p_StartDate > p_EndDate THEN
        RETURN NULL;
    END IF;
    
    -- Calculate total revenue from orders containing products from specified store
    SELECT IFNULL(SUM(o.TOTAL_MONEY), 0)
    INTO v_TotalRevenue
    FROM ORDERS o
    WHERE o.CREATED_TIMESTAMP >= p_StartDate
      AND o.CREATED_TIMESTAMP <= p_EndDate
      AND o.STATUS IN ('delivered','shipped')
      AND EXISTS (
            SELECT 1
            FROM ORDER_LINE_ITEM oli
            INNER JOIN ORDER_LINE_ITEM_VARIANT oliv
                ON oliv.LINE_ITEM_ID = oli.LINE_ITEM_ID 
               AND oliv.ORDER_ID = oli.ORDER_ID
            INNER JOIN PRODUCT_VARIANT pv
                ON pv.VARIANT_ID = oliv.VARIANT_ID 
               AND pv.PRODUCT_ID = oliv.PRODUCT_ID
            INNER JOIN PRODUCT p ON p.PRODUCT_ID = pv.PRODUCT_ID
            WHERE oli.ORDER_ID = o.ORDER_ID
              AND p.STORE_ID = p_StoreID
      );
    
    RETURN v_TotalRevenue;
END$$
