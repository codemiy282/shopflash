        CHECK (COMMISSION_RATE BETWEEN 0 AND 100),  -- Valid commission range
        CHECK (VERIFICATION_STATUS IN ('verified', 'pending', 'rejected')),  -- Valid statuses
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: BUYER
