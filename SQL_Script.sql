        CREATED_AT    DATETIME            NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- Store creation timestamp
        LOGO_IMAGE    VARCHAR(255)        NULL,      -- Store logo URL/path
        DESCRIPTION   LONGTEXT            NULL,      -- Store description
        PRIMARY KEY (STORE_ID),  -- Primary key constraint
        UNIQUE KEY SK_STORE_NAME (NAME),  -- Unique constraint for store names
