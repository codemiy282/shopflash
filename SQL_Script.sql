        NAME     VARCHAR(100)        NOT NULL,  -- Brand name (must be unique)
        PRIMARY KEY (BRAND_ID),  -- Primary key constraint
        UNIQUE KEY SK_BRAND_NAME (NAME)  -- Unique constraint for brand names
