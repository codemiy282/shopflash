    -- Tạo database nếu chưa tồn tại
    CREATE DATABASE IF NOT EXISTS Ecommerce CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
    USE Ecommerce;

    /* ==============================================================
    TABLE: EVENT_LOG
    PURPOSE: Centralized audit trail for all system activities
    ============================================================== */
    CREATE TABLE IF NOT EXISTS EVENT_LOG (
        LOG_ID          VARCHAR(20)     NOT NULL,  -- Unique log identifier (format: LOG0000001)
        EVENT_TYPE      VARCHAR(50)     NOT NULL,  -- Category: user, order, payment, product, review, etc.
        EVENT_ACTION    VARCHAR(50)     NOT NULL,  -- Action: create, update, delete, login, process, etc.
        ACTOR_ID        VARCHAR(20)     NOT NULL,  -- Who performed the action (user_id or 'system')
        TARGET_ENTITY   VARCHAR(50)     NULL,      -- Which table/entity was affected (USERS, ORDER, etc.)
        TARGET_ID       VARCHAR(50)     NULL,      -- ID of the affected record in target entity
        TIMESTAMP       DATETIME        NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- When event occurred, defaults to current time  
        CHANGE_DETAILS  LONGTEXT        NULL,      -- Detailed description of changes (JSON/text format)
        STATUS          VARCHAR(20)     NOT NULL DEFAULT 'active',    -- Log entry status: active, archived
        PRIMARY KEY (LOG_ID)  -- Primary key constraint on LOG_ID
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: USERS
    PURPOSE: Master user accounts for all platform participants
    ============================================================== */
    CREATE TABLE IF NOT EXISTS USERS (
        USER_ID         VARCHAR(10),           -- Auto-generated by trigger (format: user000001)
        USERNAME        VARCHAR(50)         NOT NULL,  -- Unique login username
        EMAIL           VARCHAR(100)        NOT NULL,  -- Unique email address for communication
        PHONE_NUMBER    VARCHAR(15)         NOT NULL,  -- Unique phone number for SMS/verification
        PASSWORD_HASH   CHAR(64)            NOT NULL,  -- SHA-256 hashed password (64 chars)
        LOGIN_BY        VARCHAR(20)         NOT NULL DEFAULT 'local',  -- Auth method: local, google, facebook
        BIRTHDATE       DATE                NOT NULL,  -- Date of birth for age verification
        STATUS          VARCHAR(10)         NOT NULL DEFAULT 'active',  -- Account status: active, inactive, banned
        CREATION_TIMESTAMP DATETIME         NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- Account creation timestamp
        PRIMARY KEY (USER_ID),  -- Primary key constraint
        UNIQUE KEY SK_USER_USERNAME (USERNAME),  -- Unique constraint for username
        UNIQUE KEY SK_USER_EMAIL (EMAIL),  -- Unique constraint for email
        UNIQUE KEY SK_USER_PHONE (PHONE_NUMBER),  -- Unique constraint for phone
        CHECK (STATUS IN ('active', 'inactive', 'banned')),  -- Valid status values
        CHECK (LOGIN_BY IN ('local', 'google', 'facebook'))  -- Valid auth methods
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: ADMIN
    PURPOSE: Administrator accounts with system management permissions
    ============================================================== */
    CREATE TABLE IF NOT EXISTS ADMIN (
        USER_ID         VARCHAR(10),           -- References USERS.USER_ID (foreign key)
        CLEARANCE_LEVEL VARCHAR(20)         NOT NULL,  -- Permission level: high, medium, low, super_admin
        AUDIT_SCOPE     VARCHAR(20)         NOT NULL,  -- Area of responsibility: user, store, payment, review
        PRIMARY KEY (USER_ID),  -- Primary key constraint
        CHECK (AUDIT_SCOPE IN ('user', 'store', 'payment', 'review')),  -- Valid scopes
        CHECK (CLEARANCE_LEVEL IN ('high', 'medium', 'low', 'super_admin')),  -- Valid levels
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: STAFF
    PURPOSE: Platform staff/employees with job details
    ============================================================== */
    CREATE TABLE IF NOT EXISTS STAFF (
        USER_ID             VARCHAR(10),           -- References USERS.USER_ID (foreign key)
        POSITION            VARCHAR(20)        NOT NULL,  -- Job position: support, order_manager
        EMPLOYMENT_STATUS   VARCHAR(20)        NOT NULL,  -- Current status: active, suspended, terminated
        SALARY              DECIMAL(18,2)      NOT NULL,  -- Monthly salary (decimal with 2 decimal places)
        DATE_ASSIGNED       DATE               NOT NULL DEFAULT (CURDATE()),  -- Employment start date, defaults to today
        DATE_ENDED          DATE               NULL,      -- Employment end date (NULL if still employed)
        PRIMARY KEY (USER_ID),  -- Primary key constraint
        CHECK (SALARY >= 0),  -- Salary cannot be negative
        CHECK (POSITION IN ('support', 'order_manager')),  -- Valid positions
        CHECK (EMPLOYMENT_STATUS IN ('active', 'suspended', 'terminated')),  -- Valid statuses
        CHECK (DATE_ENDED IS NULL OR DATE_ENDED >= DATE_ASSIGNED),  -- End date validation
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: SELLER
    PURPOSE: Seller/vendor accounts with business details
