    /* ==============================================================
    TABLE: PRODUCT
    PURPOSE: Core product information table
    ============================================================== */
    CREATE TABLE IF NOT EXISTS PRODUCT (
        PRODUCT_ID      VARCHAR(20),        -- Business-generated key (auto-generated by trigger)
        STORE_ID        INT                 NULL,  -- References STORE.STORE_ID (selling store)
        CATEGORY_ID     INT                 NULL,  -- References CATEGORY.CATEGORY_ID
        BRAND_ID        INT                 NULL,  -- References BRAND.BRAND_ID
        NAME            VARCHAR(150)        NOT NULL,  -- Product name
        DISPLAY_STATUS  VARCHAR(20)         NOT NULL,  -- Product visibility: ACTIVE, INACTIVE
        SKU             VARCHAR(50)         NOT NULL,  -- Stock Keeping Unit (must be unique)
        DESCRIPTION     LONGTEXT            NULL,      -- Product description
        QTY_IN_STOCK    INT                 NOT NULL DEFAULT 0,  -- Total quantity in stock, defaults to 0
        PRIMARY KEY (PRODUCT_ID),  -- Primary key constraint
        UNIQUE KEY SK_PRODUCT_SKU (SKU),  -- Unique constraint for SKU
        CHECK (QTY_IN_STOCK >= 0),  -- Non-negative stock quantity
        CHECK (DISPLAY_STATUS IN ('ACTIVE', 'INACTIVE')),  -- Valid statuses
        FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORY(CATEGORY_ID) ON DELETE SET NULL,  -- Set NULL if category deleted
        FOREIGN KEY (BRAND_ID) REFERENCES BRAND(BRAND_ID) ON DELETE SET NULL,  -- Set NULL if brand deleted
        FOREIGN KEY (STORE_ID) REFERENCES STORE(STORE_ID) ON DELETE SET NULL  -- Set NULL if store deleted
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ============================================================== 
    TABLE: PRODUCT_IMAGES
    PURPOSE: Multi-valued attribute for products (images)
    ============================================================== */
    CREATE TABLE IF NOT EXISTS PRODUCT_IMAGES (
        IMAGE_ID     INT AUTO_INCREMENT,  -- Auto-increment global image ID
        PRODUCT_ID   VARCHAR(20)       NOT NULL,  -- References PRODUCT.PRODUCT_ID
        NAME         VARCHAR(255)      NOT NULL,  -- Image file name or URL/path
        PRIMARY KEY (PRODUCT_ID, NAME),  -- Composite PK: 
        UNIQUE KEY (IMAGE_ID),  -- IMAGE_ID must be unique and defined as a key for auto-increment
        UNIQUE (NAME),
        FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID) ON DELETE CASCADE  -- Cascade delete when product deleted
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: PRODUCT_VARIANT
    PURPOSE: Store different variations of products (colors, sizes)
    ============================================================== */
    CREATE TABLE IF NOT EXISTS PRODUCT_VARIANT (
        VARIANT_ID         INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        PRODUCT_ID         VARCHAR(20)          NOT NULL,  -- References PRODUCT.PRODUCT_ID (parent product)
        LIST_PRICE         DECIMAL(10,2)        NOT NULL,  -- Original/regular price
        VALIDITY_METHOD    VARCHAR(30)          NOT NULL DEFAULT 'standard',  -- Pricing method: standard, discounted, promotion
        VALID_FROM         DATE                 NULL,      -- Price validity start date
        VALID_TO           DATE                 NULL,      -- Price validity end date
        AVAILABLE_QTY      INT                  NOT NULL DEFAULT 0,  -- Quantity available for sale, defaults to 0
        RESERVED_QTY       INT                  NOT NULL DEFAULT 0,  -- Quantity reserved in pending orders, defaults to 0
        ALERT_THRESHOLD    INT                  NOT NULL DEFAULT 5,  -- Low stock alert threshold, defaults to 5
        DISCOUNT_PRICE     DECIMAL(10,2)        NOT NULL DEFAULT 0,  -- Current discounted price, defaults to 0
        COLOR              VARCHAR(50)          NULL,      -- Variant color
        SIZE               VARCHAR(20)          NULL,      -- Variant size
        BARCODE            VARCHAR(50)          NOT NULL,  -- Unique barcode (must be unique)
        WEIGHT             DECIMAL(10,2)        NULL,      -- Product weight (in kg/lb)
        PRIMARY KEY (VARIANT_ID, PRODUCT_ID),  -- Composite primary key (weak entity)
        UNIQUE KEY SK_VARIANT_BARCODE (BARCODE),  -- Unique constraint for barcodes
        CHECK (WEIGHT >= 0),  -- Non-negative weight
        CHECK (LIST_PRICE >= 0),  -- Non-negative list price
        CHECK (AVAILABLE_QTY >= 0 AND RESERVED_QTY >= 0 AND ALERT_THRESHOLD >= 0),  -- Non-negative stock values
        CHECK (VALIDITY_METHOD IN ('standard', 'discounted', 'promotion')),  -- Valid pricing methods
        FOREIGN KEY (PRODUCT_ID) REFERENCES PRODUCT(PRODUCT_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: PROMOTION
    PURPOSE: Discount promotions and sales campaigns
    ============================================================== */
    CREATE TABLE IF NOT EXISTS PROMOTION (
        PROMOTION_ID          INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        DESCRIPTION           LONGTEXT           NOT NULL,  -- Promotion description
        DISCOUNT_TYPE         VARCHAR(20)        NOT NULL,  -- Discount type: percentage, fixed, free_shipping
        DISCOUNT_VALUE        DECIMAL(10,2)      NOT NULL,  -- Discount amount/percentage value
        MAX_DISCOUNT_AMOUNT   DECIMAL(10,2)      NULL,      -- Maximum discount cap (for percentage discounts)
        USAGE_LIMIT           INT                NULL,      -- Maximum usage limit (NULL = unlimited)
        STATUS                VARCHAR(20)        NOT NULL,  -- Promotion status: active, expired, disabled
        START_DATE            DATETIME           NOT NULL,  -- Promotion start date
        END_DATE              DATETIME           NOT NULL,  -- Promotion end date
        PRIMARY KEY (PROMOTION_ID),  -- Primary key constraint
        CHECK (USAGE_LIMIT >= 0),  -- Non-negative usage limit
        CHECK (START_DATE < END_DATE),  -- Valid date range (start before end)
        CHECK (DISCOUNT_VALUE >= 0),  -- Non-negative discount value
