    /* ==============================================================
    TABLE: SESSIONS
    PURPOSE: Track user login sessions and activity
    ============================================================== */
    CREATE TABLE IF NOT EXISTS SESSIONS (
        SESSION_ID  INT AUTO_INCREMENT,   -- Auto-increment primary key (surrogate key)
        USER_ID     VARCHAR(10)         NOT NULL,  -- References USERS.USER_ID
        IP_ADDRESS  VARCHAR(45)         NOT NULL,  -- IPv4 or IPv6 address of user
        DEVICE_INFO VARCHAR(500)        NULL,      -- Device/browser information (user agent)
        START_TIME  DATETIME            NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- Session start timestamp
        END_TIME    DATETIME            NULL,      -- Session end timestamp (NULL = active session)
        STATUS      VARCHAR(20)         NOT NULL DEFAULT 'active',  -- Session status: active, expired, terminated, invalid
        PRIMARY KEY (SESSION_ID),  -- Primary key constraint
        CHECK (STATUS IN ('active', 'expired', 'terminated', 'invalid')),  -- Valid statuses
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: ACTIONS
    PURPOSE: Track user actions within sessions (analytics)
    ============================================================== */
    CREATE TABLE IF NOT EXISTS ACTIONS (
        ACTION_ID       INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        SESSION_ID      INT              NOT NULL,  -- References SESSIONS.SESSION_ID
        ACTION_NAME     VARCHAR(100)     NOT NULL,  -- Name/type of action (login, view_product, add_to_cart, etc.)
        TIMESTAMP       DATETIME         NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- When action occurred
        ACTION_DETAILS  LONGTEXT         NULL,      -- Additional action details (JSON/XML format)
        PAGE_URL        VARCHAR(500)     NULL,      -- URL where action occurred
        REFERRER_URL    VARCHAR(500)     NULL,      -- Referring URL (previous page)
        PRIMARY KEY (ACTION_ID),  -- Primary key constraint
        FOREIGN KEY (SESSION_ID) REFERENCES SESSIONS(SESSION_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: SUPPORT_INBOX
    PURPOSE: Customer support ticket management system
    ============================================================== */
    CREATE TABLE IF NOT EXISTS SUPPORT_INBOX (
        MESSAGE_ID              INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        USER_ID                 VARCHAR(10)        NOT NULL,     -- References USERS.USER_ID (message sender)
        SUBJECT                 VARCHAR(200)       NOT NULL,     -- Ticket subject/heading
        MESSAGE_BODY            LONGTEXT           NOT NULL,     -- Detailed message content
        SLA_MILESTONES          LONGTEXT           NULL,         -- Service Level Agreement tracking (JSON format)
        LABELS                  VARCHAR(500)       NULL,         -- Categorization labels (comma-separated)
        EXCHANGES_CHAT_HISTORY  LONGTEXT           NULL,         -- Full conversation history (JSON format)
        REPLY_TEMPLATES         LONGTEXT           NULL,         -- Available reply templates (JSON format)
        PRIORITY                VARCHAR(20)        NOT NULL DEFAULT 'normal',  -- Ticket priority: low, normal, high, urgent
        STATUS                  VARCHAR(20)        NOT NULL DEFAULT 'open',    -- Ticket status: open, in_progress, resolved, closed
        CREATED_AT              DATETIME           NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- Ticket creation timestamp
        UPDATED_AT              DATETIME           NULL,        -- Last update timestamp
        ASSIGNED_TO             VARCHAR(10)        NULL,        -- References USERS.USER_ID (staff assigned)
        RESOLVED_AT             DATETIME           NULL,        -- Resolution timestamp
        PRIMARY KEY (MESSAGE_ID),  -- Primary key constraint
        CHECK (PRIORITY IN ('low', 'normal', 'high', 'urgent')),  -- Valid priority values
        CHECK (STATUS IN ('open', 'in_progress', 'resolved', 'closed')),  -- Valid status values
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: COUNTRY
