    /* ==============================================================
    TRIGGER: trg_user_id
    PURPOSE: Auto-generate USER_ID with 'user' prefix
    DESCRIPTION: 
    - Generates USER_ID in format: user000001, user000002, etc.
    - Uses BEFORE INSERT to intercept inserts and add generated IDs
    - Maintains sequential numbering based on existing highest ID
    ============================================================== */
    DELIMITER $$
    CREATE TRIGGER trg_user_id
    BEFORE INSERT ON USERS
    FOR EACH ROW
    BEGIN
        DECLARE prefix VARCHAR(4) DEFAULT 'user';
        DECLARE max_num INT DEFAULT 0;
        DECLARE next_num INT DEFAULT 0;
        
        -- Find the highest existing user number by extracting numeric part from USER_ID
        SELECT IFNULL(MAX(CAST(SUBSTRING(USER_ID, 5, 6) AS UNSIGNED)), 0)
        INTO max_num
        FROM USERS;
        
        -- Generate next number
        SET next_num = max_num + 1;
        
        -- Set new USER_ID
        SET NEW.USER_ID = CONCAT(prefix, LPAD(next_num, 6, '0'));
    END$$
    DELIMITER ;

