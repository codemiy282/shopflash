    /* ==============================================================
    TABLE: ADDRESS
    PURPOSE: Store user addresses for shipping and billing
    ============================================================== */
    CREATE TABLE IF NOT EXISTS ADDRESS (
        ADDRESS_ID    INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        COUNTRY_ID    INT                 NOT NULL,  -- References COUNTRY.COUNTRY_ID
        USER_ID       VARCHAR(10)         NOT NULL,  -- References USERS.USER_ID (address owner)
        UNIT_NUMBER   VARCHAR(50)         NULL,      -- Apartment/unit/suite number
        STREET_NUMBER VARCHAR(50)         NULL,      -- Street/house number
        REGION        VARCHAR(100)        NULL,      -- State/Province/Region
        CITY          VARCHAR(100)        NULL,      -- City name
        ADDRESS_LINE  VARCHAR(255)        NOT NULL,  -- Main address line (street name, area)
        POSTAL_CODE   VARCHAR(20)         NULL,      -- ZIP/Postal code
        PRIMARY KEY (ADDRESS_ID),  -- Primary key constraint
        FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY(COUNTRY_ID) ON DELETE NO ACTION,  -- Prevent country deletion
        FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: LOGISTICS_PARTNER
    PURPOSE: Store logistics/shipping company information
    ============================================================== */
    CREATE TABLE IF NOT EXISTS LOGISTICS_PARTNER (
        PARTNER_ID           INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        NAME                 VARCHAR(100)       NOT NULL,  -- Company name (must be unique)
        RATING               DECIMAL(3,2)       NULL,      -- Customer rating (0.00-5.00 scale)
        CONTACT_INFO         VARCHAR(255)       NULL,      -- Contact information (phone, email)
        HEADQUARTERS_ADDRESS VARCHAR(255)       NULL,      -- Company headquarters address
        COVERAGE_AREA        LONGTEXT           NULL,      -- Service coverage areas (JSON/text)
        CONTRACT_TERMS       LONGTEXT           NULL,      -- Contract terms and conditions
        PRIMARY KEY (PARTNER_ID),  -- Primary key constraint
        UNIQUE KEY SK_LOGISTICS_NAME (NAME),  -- Unique constraint for partner names
        CHECK (RATING BETWEEN 0 AND 5)  -- Rating validation (0-5)
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: SERVICE_TYPES
    PURPOSE: Store available shipping service types for logistics partners
    ============================================================== */
    CREATE TABLE IF NOT EXISTS SERVICE_TYPES (
        SERVICE_TYPE        VARCHAR(50),  -- Type of service: Express, Standard, Economy, etc.
        PARTNER_ID          INT,           -- References LOGISTICS_PARTNER.PARTNER_ID
        PRIMARY KEY (SERVICE_TYPE, PARTNER_ID),  -- Composite primary key
        FOREIGN KEY (PARTNER_ID) REFERENCES LOGISTICS_PARTNER(PARTNER_ID) ON DELETE CASCADE  -- Cascade delete
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: SHIPMENT
    PURPOSE: Track shipment details for customer orders
    ============================================================== */
    CREATE TABLE IF NOT EXISTS SHIPMENT (
        SHIPMENT_ID        INT AUTO_INCREMENT,  -- Auto-increment primary key (surrogate key)
        PARTNER_ID         INT                 NULL,      -- References LOGISTICS_PARTNER.PARTNER_ID
        LOG_ID             VARCHAR(20)         NULL,      -- References EVENT_LOG.LOG_ID (for audit trail)
        TRACKING_CODE      VARCHAR(50)         NOT NULL,  -- Unique tracking number (must be unique)
        SERVICE_PACKAGE    VARCHAR(100)        NULL,      -- Service type/package name
        CARRIER            VARCHAR(100)        NULL,      -- Carrier/company name
        SHIPPING_FEE       DECIMAL(18,2)       NOT NULL DEFAULT 0,  -- Shipping cost, defaults to 0
        CREATION_TIMESTAMP DATETIME            NOT NULL DEFAULT CURRENT_TIMESTAMP,  -- Shipment creation timestamp
        DELIVERED_DATE     DATETIME            NULL,      -- Actual delivery timestamp
        PROOF_OF_DELIVERY  LONGTEXT            NULL,      -- Delivery proof (signature image/receipt)
        MILESTONES         LONGTEXT            NULL,      -- Shipment progress tracking (JSON)
        STATUS             VARCHAR(20)         NOT NULL,  -- Current status: delivered, delivering, processing, returned
        PRIMARY KEY (SHIPMENT_ID),  -- Primary key constraint
        UNIQUE KEY SK_SHIPMENT_TRACKING_CODE (TRACKING_CODE),  -- Unique constraint for tracking codes
        CHECK (SHIPPING_FEE >= 0),  -- Non-negative shipping fee
        CHECK (STATUS IN ('delivered','delivering','processing','returned')),  -- Valid statuses
        FOREIGN KEY (PARTNER_ID) REFERENCES LOGISTICS_PARTNER(PARTNER_ID) ON DELETE SET NULL,  -- Set NULL if partner deleted
        FOREIGN KEY (LOG_ID) REFERENCES EVENT_LOG(LOG_ID) ON DELETE SET NULL  -- Set NULL if log deleted
    ) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

    /* ==============================================================
    TABLE: STORE
