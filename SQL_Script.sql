DELIMITER $$
CREATE PROCEDURE sp_CreateBuyerUser (
    IN p_Username      VARCHAR(50),
    IN p_Email         VARCHAR(100),
    IN p_Phone         VARCHAR(15),
    IN p_PasswordHash  CHAR(64),
    IN p_Birthdate     DATE
)
BEGIN
    DECLARE v_UserID VARCHAR(10);
    DECLARE v_ErrorMsg VARCHAR(500);
    
    -- Handler for errors
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        ROLLBACK;
        RESIGNAL;
    END;
    
    START TRANSACTION;

    -- Check if user already exists
    SELECT USER_ID INTO v_UserID
    FROM USERS
    WHERE EMAIL = p_Email
       OR PHONE_NUMBER = p_Phone
       OR USERNAME = p_Username
    LIMIT 1;

    -- If user doesn't exist, create new user
    IF v_UserID IS NULL THEN
        -- Insert new user (trigger will auto-generate USER_ID)
        INSERT INTO USERS (USERNAME, EMAIL, PHONE_NUMBER, PASSWORD_HASH, LOGIN_BY, BIRTHDATE, STATUS)
        VALUES (p_Username, p_Email, p_Phone, p_PasswordHash, 'local', p_Birthdate, 'active');

        -- Get the auto-generated USER_ID
        SET v_UserID = (SELECT USER_ID FROM USERS WHERE EMAIL = p_Email LIMIT 1);
    ELSE
        -- User exists, check if already a buyer
        IF EXISTS (SELECT 1 FROM BUYER WHERE USER_ID = v_UserID) THEN
            SET v_ErrorMsg = CONCAT('User already exists as buyer with ID: ', v_UserID);
            SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'User already exists as buyer.';
        END IF;
    END IF;

    -- Create buyer record
    INSERT INTO BUYER (USER_ID, NAME, PHONE_NUMBER)
    VALUES (v_UserID, p_Username, p_Phone);
    
    COMMIT;

    -- Return the user ID
    SELECT v_UserID AS NewUserID, 'Buyer created successfully' AS Message;
END$$

